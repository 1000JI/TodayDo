import Foundation
import UIKit

func fontCoreDreamHeavy(fontSize:Int) -> UIFont {
    return UIFont(name: "S-CoreDream-8Heavy", size: CGFloat(fontSize))!
}

func fontCoreDreamLight(fontSize:Int) -> UIFont {
    return UIFont(name: "S-CoreDream-3Light", size: CGFloat(fontSize))!
}
