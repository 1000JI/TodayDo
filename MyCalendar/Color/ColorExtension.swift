import Foundation
import UIKit

extension UIColor {
    var customGreen : UIColor {
       return UIColor(red: 48/255, green: 191/255, blue: 71/255, alpha: 1)
    }
    var customRed : UIColor {
       return UIColor(red: 252/255, green: 32/255, blue: 37/255, alpha: 1)
    }
    var customBlue : UIColor {
       return UIColor(red: 9/255, green: 96/255, blue: 254/255, alpha: 1)
    }
    var customYellow : UIColor {
       return UIColor(red: 254/255, green: 194/255, blue: 11/255, alpha: 1)
    }
    var customGray : UIColor {
       return UIColor(red: 229/255, green: 229/255, blue: 233/255, alpha: 1)
    }
    var customPink : UIColor {
        return UIColor(red: 242/255, green: 190/255, blue: 189/255, alpha: 0.2)
    }
    var customPinkBar : UIColor {
        return UIColor(red: 250/255, green: 238/255, blue: 238/255, alpha: 0.2)
    }
}
